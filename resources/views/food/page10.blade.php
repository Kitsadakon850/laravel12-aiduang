คำบรรยายภาพ,ต้องใช้เวลาหลายปีในการตรวจสอบการสแกน 3 มิติอย่างครบถ้วน
โศกนาฏกรรมของมนุษย์จากไททานิคยังคงมองเห็นได้ชัดเจน

