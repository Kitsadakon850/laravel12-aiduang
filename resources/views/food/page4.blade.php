    <h3>
นักวิทยาศาสตร์ค้นพบหลักฐานที่มีความหนักแน่นบ่งชี้ถึงดาวเคราะห์ก๊าซยักษ์ในระบบดาวฤกษ์ใกล้กับระบบสุริยะของเรา

ด้วยระยะห่างราว 4.5 ปีแสง ดาวเคราะห์ที่พบว่ามีไม่สิ่งมีชีวิตดวงนี้ อาจจะเป็นเพื่อนบ้านที่ใกล้ชิดกับโลกในแง่ดาราศาสตร์ และอาจมีดวงจันทร์ที่สามารถเป็นที่อยู่อาศัยของสิ่งมีชีวิตได้

กล้องโทรทรรศน์อวกาศเจมส์ เวบบ์ อันทรงพลังสามารถตรวจจับสัญญาณดาวเคราะห์ดวงนี้ในระบบดาวที่มีชื่อว่า อัลฟา เซนทอรี (Alpha Centauri) ได้เมื่อปีที่แล้ว ก่อนที่มันจะหายไปในการสังเกตการณ์ติดตามในภายหลัง

จึงทำให้นักดาราศาสตร์ต้องสังเกตการณ์อีกครั้งเพื่อพิสูจน์ว่า มันมีอยู่จริง

การค้นพบครั้งนี้ทำให้นักวิทยาศาสตร์รู้สึกตื่นเต้น โดยเฉพาะเพื่อพบว่ามีความคล้ายคลึงกันระหว่างดาวฤกษ์ของดาวเคราะห์นอกระบบสุริยะกับดวงอาทิตย์ของเรา

