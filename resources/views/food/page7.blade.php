คันคูลู (Khankhuuluu) เป็นตัวแทนของการเปลี่ยนแปลงทางวิวัฒนาการที่เปลี่ยนจากการเป็นนักล่าตัวเล็กที่สามารถวิ่งได้อย่างว่องไวในยุคจูราสสิก ไปสู่ไดโนเสาร์ยักษ์ใหญ่ที่น่าเกรงขามอย่างทีเร็กซ์

คันคูลูน่าจะมีน้ำหนักตัวประมาณ 750 กิโลกรัม ขณะที่ทีเร็กซ์ตัวเต็มวัยอาจมีน้ำหนักมากถึง 8 เท่าของไดโนเสาร์คันคูลู ดังนั้น "ซากฟอสซิลนี้จึงเป็นฟอสซิลในช่วงรอยต่อระหว่างบรรพบุรุษยุคแรกกับไทแรนโนซอรัสที่ยิ่งใหญ่ในเวลาต่อมา" ศาสตราจารย์เซเลนิทสกีอธิบาย

"มันช่วยให้เรากลับมาทบทวนแผนภูมิลำดับเครือญาติของไทแรนโนซอรัสและเขียนสิ่งที่เรารู้เกี่ยวกับวิวัฒนาการของพวกมันขึ้นใหม่" นักบรรพชีวินวิทยาหญิงคนดังกล่าว กล่าวเสริม

