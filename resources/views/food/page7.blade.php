คำบรรยายภาพ,จาเร็ด วอริส นักศึกษาปริญญาเอกหัวหน้าทีมวิจัยร่วม และศาสตราจารย์เซเลนิทสกี ตรวจสอบซากฟอสซิลของไดโนเสาร์ไทแรนโนซอร์
ไดโนเสาร์สายพันธุ์ใหม่ที่เพิ่งค้นพบยังแสดงให้เห็นการวิวัฒนาการในช่วงแรก ๆ ของลักษณะสำคัญที่เป็นหัวใจของความโหดเหี้ยมของไทแรนโนซอรัส เช่น กายวิภาคของกะโหลกศีรษะของพวกมันที่ทำให้มันมีขากรรไกรที่แข็งแรง จาเร็ด วอริส หัวหน้าทีมวิจัยร่วมอธิบายว่า "เราเห็นลักษณะต่าง ๆ ของกระดูกบริเวณสันจมูกที่ทำให้พวกไทแรนโนซอรัสมีแรงขบกัดที่มีพละกำลังมหาศาล"

วิวัฒนาการของขากรรไกรที่แข็งแกร่งทำให้ไดโนเสาร์ทีเร็กซ์สามารถตะครุบเหยื่อที่ตัวใหญ่กว่าได้และทำได้กระทั่งการกัดทะลุกระดูก

ซากโครงกระดูกสองส่วนที่ทีมนักวิจัยได้ตรวจสอบในการศึกษาวิจัยครั้งนี้ถูกค้นพบครั้งแรกในมองโกเลียตั้งแต่ช่วงต้นทศวรรษที่ 1970 ในตอนแรกพวกมันถูกจัดให้อยู่ในสายพันธุ์เดิมที่เคยค้นพบมาแล้วที่ชื่อว่า "อเล็กโทรซอรัส" (Alectrosaurus) แต่เมื่อจาเร็ด วอริส ได้ตรวจสอบอีกครั้ง เขาก็พบลักษณะที่มีความคล้ายกับสายพันธุ์ไทแรนโนซอรัสที่ทำให้มันแตกต่างออกไปจากที่เคยถูกจำแนกไว้ตอนแรก

"ดิฉันจำตอนที่ได้ข้อความจากเขาส่งมาได้ ที่เขาคิดว่ามันคือสายพันธุ์ใหม่" ศ.เซเลนิทสกี ย้อนความจำ

ข้อเท็จจริงที่ว่า ไดโนเสาร์สายพันธุ์นี้สามารถอพยพเคลื่อนย้ายระหว่างทวีปอเมริกาเหนือและเอเชียได้ผ่านเส้นทางที่เชื่อมต่อทางบกระหว่างไซบีเรียและอะแลสกาในเวลานั้น ได้ช่วยให้ทีมนักวิทยาศาสตร์ค้นพบความแตกต่างนี้ได้

วอริสอธิบายว่า "การอพยพย้ายกลับไปกลับมาระหว่างทวีปผลักดันให้ไทแรนโนซอรัสวิวัฒนาการขึ้นมาเป็นไทแรนซอรัสที่แตกต่างกัน" ในช่วงเวลาหลายล้านปี

ด้าน ศ.เซเลนิทสกี กล่าวเสริมว่า "การค้นพบนี้แสดงให้เราเห็นว่า ก่อนที่ไทแรนโนซอรัสจะกลายเป็นราชา [แห่งไดโนเสาร์] พวกมันต่างเป็นเจ้าชายมาก่อน"
