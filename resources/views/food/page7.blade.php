นักวิทยาศาสตร์ค้นพบสายพันธุ์ใหม่ของไดโนเสาร์ ซึ่งเป็นหนึ่งในคอลเลคชันที่จัดแสดงในพิพิธภัณฑ์แห่งหนึ่งในมองโกเลีย โดยพวกเขาระบุว่า นี่ถือเป็นการเขียนประวัติวิวัฒนาการของไดโนเสาร์ไทแรนโนซอรัส เร็กซ์ (Tyrannosaurus rex) หรือ ทีเร็กซ์ ขึ้นใหม่

คณะนักวิจัยสรุปว่า โครงกระดูกสองชิ้นซึ่งมีอายุถึง 86 ล้านปีที่พวกเขาได้ศึกษา เป็นโครงกระดูกของสายพันธุ์ไดโนเสาร์ที่ถือว่าเป็นบรรพบุรุษที่ใกล้ชิดที่สุดของไทแรนโนซอรัส เร็กซ์ ทั้งหมด ซึ่งเป็นไดโนเสาร์นักล่าที่รวมทั้งตัวทีเร็กซ์อันโด่งดังด้วย

นักวิจัยตั้งชื่อสายพันธุ์ใหม่ที่ค้นพบนี้ว่า คันคูลู มองโกเลียนซิส (Khankhuuluu mongoliensis) ซึ่งมีความหมายว่า "เจ้าชายมังกรแห่งมองโกเลีย"

การค้นพบซึ่งได้รับการตีพิมพ์ในวารสารเนเจอร์ ถือเป็นการเปิดโอกาสให้เราได้สำรวจวิวัฒนาการสู่การเป็นนักล่าที่ทรงพลังของไดโนเสาร์ไทแรนโนซอรัส เร็กซ์ ที่ครองดินแดนทวีปอเมริกาเหนือและเอเชีย จนกระทั่งถึงยุคสุดท้ายของไดโนเสาร์

A pristine fossilised skeleton of Tyrannosaurus Rex. Its body lies encased in the rock it was fossilised within and it stretches out from its long tail on the left of the fossil to its open jaws.  ที่มาของภาพ,Darla Zelenitsky
คำบรรยายภาพ,แม้ว่าจะมีฟอสซิลที่สวยงามและสมบูรณ์ซึ่งให้ภาพที่ชัดเจนของทีเร็กซ์(อย่างที่เห็นในภาพ) แต่บรรพบุรุษในยุคก่อนนั้นมีความลึกลับมากกว่า
"การตั้งชื่อ 'เจ้าชาย' หมายถึงการที่มันเป็นไดโนเสาร์สายไทแรนโนซอรอยด์ในยุคแรก ๆ ที่มีขนาดเล็กกว่า" ศาสตราจารย์ดาร์ลา เซเลนิทสกี นักบรรพชีวินวิทยาจากมหาวิทยาลัยคัลการีในแคนาดา อธิบาย โดยไทแรนโนซอรอยด์คือ ซูเปอร์แฟมิลีหรือวงศ์ใหญ่ของไดโนเสาร์กินเนื้อที่เดินด้วยสองขา

