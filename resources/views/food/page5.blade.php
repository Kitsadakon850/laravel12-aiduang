คำบรรยายภาพ,คลื่นความร้อนกําลังเพิ่มขึ้นและรุนแรงมากขึ้นในหลายที่ทั่วโลก และมันขับเคลื่อนจากความเปลี่ยนแปลงสภาพภูมิอากาศ
คลื่นความร้อนโดยเฉพาะอย่างยิ่งกรณีที่อุณหภูมิสูงขึ้นในเวลากลางคืน สามารถส่งผลต่อการนอนหลับของผู้คน ส่งผลกระทบต่ออารมณ์ของเราและอาจทำให้อาการป่วยแย่ลง "สำหรับคนจำนวนมากที่เป็นโรคลมบ้าหมู การนอนหลับที่ไม่ดีสามารถเพิ่มความเสี่ยงต่อการเป็นลมชักได้" ซิโซดิยา กล่าว

หลักฐานชี้ให้เห็นว่าการเข้ารักษาตัวในโรงพยาบาลและอัตราการตายในหมู่ผู้ที่มีภาวะสมองเสื่อมนั้นเพิ่มมากขึ้นในช่วงที่เกิดคลื่นความร้อน ส่วนหนึ่งก็อาจเป็นเพราะอายุ เพราะผู้สูงอายุควบคุมอุณหภูมิของร่างกายได้น้อยลง แต่อาการบกพร่องทางการรับรู้ของพวกเขาอาจทำให้ไม่สามารถปรับตัวเข้ากับความร้อนสูงได้หากพวกเขาดื่มน้ำไม่พอ ลืมปิดหน้าต่าง หรือออกไปข้างนอกในที่ที่อากาศร้อนอบอ้าวทั้งที่ไม่ควร

อุณหภูมิที่สูงขึ้นยังเชื่อมโยงกับการเพิ่มขึ้นของอุบัติการณ์และอัตราการเสียชีวิตจากโรคหลอดเลือดสมอง ในการศึกษาหนึ่งที่วิเคราะห์ข้อมูลการเสียชีวิตจากโรคหลอดเลือดสมองจาก 25 ประเทศ นักวิจัยพบว่าในบรรดาผู้เสียชีวิตจากโรคหลอดเลือดสมองตีบ 1,000 ราย ช่วงที่อากาศร้อนจัดมากที่สุดมีส่วนทำให้มีผู้เสียชีวิตเพิ่มขึ้นมา 2 ราย

"นั่นอาจดูเหมือนไม่มากนัก" เบธาน เดวีส์ แพทย์ผู้เชี่ยวชาญด้านผู้สูงอายุประจำโรงพยาบาลมหาวิทยาลัยซัสเซกซ์ ในสหราชอาณาจักรกล่าว "แต่เนื่องจากมีผู้เสียชีวิตจากโรคหลอดเลือดสมองทั่วโลกถึง 7 ล้านคนต่อปี ความร้อนอาจเป็นปัจจัยหนึ่งที่ทำให้มีผู้เสียชีวิตจากโรคหลอดเลือดสมองเพิ่มขึ้นมากกว่า 10,000 รายต่อปี" เดวีส์และผู้ร่วมเขียนหนังสือเตือนว่า การเปลี่ยนแปลงสภาพภูมิอากาศมีแนวโน้มที่จะทำให้สถานการณ์นี้แย่ลงในช่วงหลายปีข้างหน้า

ภาระจากโรคหลอดเลือดสมองที่เกี่ยวข้องกับความร้อนจะตกอยู่กับประเทศที่มีรายได้ปานกลางและรายได้น้อย ซึ่งเป็นประเทศที่ได้รับผลกระทบจากการเปลี่ยนแปลงสภาพภูมิอากาศมากที่สุดอยู่แล้วและมีอัตราการเกิดโรคหลอดเลือดสมองสูงที่สุด

"อุณหภูมิที่สูงขึ้นจะทำให้ความเหลื่อมล้ำทางสุขภาพรุนแรงขึ้นทั้งระหว่างประเทศและภายในกลุ่มทางสังคมในประเทศ" เดวีส์กล่าว หลักฐานที่เพิ่มมากขึ้นบ่งชี้ว่าผู้สูงอายุ รวมถึงผู้ที่มีฐานะทางเศรษฐกิจและสังคมต่ำ มีความเสี่ยงต่อการเสียชีวิตที่เกี่ยวข้องกับความร้อนเพิ่มขึ้น

โลกที่ร้อนขึ้นยังส่งผลเสียต่อพัฒนาการทางระบบประสาทของเด็กเล็กอีกด้วย

"มีการเชื่อมโยงระหว่างความร้อนจัดกับผลลัพธ์ที่ไม่ดีของการตั้งครรภ์ เช่น การคลอดก่อนกำหนด" เจน เฮิร์สต์ ศาสตราจารย์ด้านสุขภาพสตรีทั่วโลกจากมหาวิทยาลัยอิมพีเรียลคอลเลจลอนดอน ในสหราชอาณาจักร กล่าว

ผลการศึกษาวิจัยทางวิทยาศาสตร์อย่างเป็นระบบล่าสุดพบว่า คลื่นความร้อนสัมพันธ์กับอัตราการคลอดก่อนกำหนดที่เพิ่มขึ้น 26% ซึ่งอาจนำไปสู่ความล่าช้าทางพัฒนาการทางระบบประสาทและความบกพร่องทางสติปัญญา

"อย่างไรก็ตาม ยังมีอีกหลายสิ่งที่เรายังไม่รู้" ศ.เฮิร์สต์กล่าว พร้อมเสริมด้วยว่า "ใครมีความเสี่ยงมากที่สุดและเพราะเหตุใด ? เพราะเห็นได้ชัดว่ามีผู้หญิง 130 ล้านคนที่คลอดบุตรทุกปี และส่วนใหญ่อยู่ในพื้นที่ที่มีอากาศร้อน แต่ผลข้างเคียงเหล่านี้ก็ไม่ได้เกิดกับพวกเธอ"

ความร้อนที่สูงเกินไปจากการเปลี่ยนแปลงสภาพภูมิอากาศอาจทำให้สมองเกิดความเครียดเพิ่มขึ้นทำให้สมองเปราะบางต่อการเกิดความเสียหายที่อาจนำไปสู่โรคระบบประสาทเสื่อม

ความร้อนยังส่งผลกระทบต่อเกราะป้องกันสมอง ทำให้เกราะป้องกันนี้ถูกซึมผ่านได้มากขึ้นและเพิ่มความเสี่ยงต่อการที่สารพิษ แบคทีเรีย และไวรัสจะเข้าสู่เนื้อเยื่อสมองของเราได้

เรื่องนี้อาจมีความสำคัญมากขึ้นเมื่ออุณหภูมิสูงขึ้น เช่นเดียวกับการแพร่ระบาดของยุงที่แพร่เชื้อไวรัสที่ก่อให้เกิดโรคทางระบบประสาท เช่น ไวรัสซิกา ชิคุนกุนยา และไข้เลือดออก "ไวรัสซิกาสามารถส่งผลกระทบต่อทารกในครรภ์และทำให้เกิดภาวะศีรษะเล็กได้" โทเบียส ซูเตอร์ นักกีฏวิทยาการแพทย์ประจำสถาบันสุขภาพเขตร้อนและสาธารณะแห่งสวิสกล่าว

"อุณหภูมิที่สูงขึ้นและฤดูหนาวที่อากาศอบอุ่นขึ้น หมายความว่าฤดูผสมพันธุ์ของยุงจะเริ่มเร็วขึ้นในปีนั้นและสิ้นสุดลงช้าลง" นักกีฏวิทยารายนี้ กล่าวเสริม

