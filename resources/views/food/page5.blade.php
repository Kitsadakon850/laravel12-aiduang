ซันเจย์ ซิโซดิยา จากมหาวิทยาลัย ยูนิเวอร์ซิตี คอลเลจ ลอนดอน หรือยูซีแอล ซึ่งเป็นผู้บุกเบิกการศึกษาผลกระทบของการเปลี่ยนแปลงสภาพภูมิอากาศต่อสมอง กล่าวว่าโรคลมชักในกลุ่มอาการดราเว็ต (Dravet syndrome) เป็นหนึ่งในโรคทางระบบประสาทที่ผู้ที่ป่วยจะมีอาการแย่ลงเมื่ออุณหภูมิสูงขึ้น

ในฐานะนักประสาทวิทยาผู้เชี่ยวชาญด้านโรคลมชัก เขามักได้ยินจากครอบครัวของผู้ป่วยว่ามีปัญหามากขึ้นในช่วงที่เกิดคลื่นความร้อน

"แน่นอนว่า ผมคิดกับตัวเองว่าทำไมการเปลี่ยนแปลงสภาพภูมิอากาศถึงจะไม่ส่งผลกระทบต่อสมองด้วยล่ะ ในเมื่อกระบวนการมากมายในสมองมีส่วนเกี่ยวข้องกับวิธีที่ร่างกายรับมือกับความร้อน" ซิโซดิยา กล่าว

ระหว่างที่ผู้เชี่ยวชาญจากยูซีแอลศึกษาค้นคว้าเอกสารทางวิทยาศาสตร์ เขาได้ค้นพบภาวะทางระบบประสาทหลายอย่างที่แย่ลงจากความร้อนและความชื้นที่สูงขึ้น ได้แก่ โรคลมชัก โรคหลอดเลือดสมอง โรคสมองอักเสบ โรคปลอกประสาทเสื่อมแข็ง ไมเกรน และโรคอื่น ๆ อีกมากมาย

เขาค้นพบด้วยว่าผลกระทบของการเปลี่ยนแปลงสภาพภูมิอากาศต่อสมองของเราเริ่มปรากฏให้เห็นอย่างชัดเจนแล้ว

ยกตัวอย่างเช่น ในช่วงเกิดคลื่นความร้อนในยุโรปเมื่อปี 2003 ประมาณ 7% ของผู้เสียชีวิตที่สูงเกินปกติ เสียชีวิตด้วยสาเหตุจากปัญหาทางระบบประสาทโดยตรง ตัวเลขที่คล้ายคลึงกันนี้ยังพบในช่วงคลื่นความร้อนในสหราชอาณาจักรในปี 2022 ด้วย

แต่ความร้อนยังสามารถเปลี่ยนแปลงการทำงานของสมองในรูปแบบอื่น ๆ ได้ด้วย เช่น ทำให้เรามีอารมณ์รุนแรง หงุดหงิด และซึมเศร้ามากขึ้น

ดังนั้นแล้วเมื่อโลกร้อนขึ้นจากการเปลี่ยนแปลงสภาพภูมิอากาศ จะเกิดผลกระทบต่อสมองของเราอย่างไรได้บ้าง

