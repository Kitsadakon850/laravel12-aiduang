คำบรรยายภาพ,คลื่นความร้อนที่รุนแรง สามารถส่งผลกระทบต่อวิธีการทำงานของสมอง นำไปสู่การเปลี่ยนแปลงทางพฤติกรรม อารมณ์ และมักจะทำให้โรคเกี่ยวกับระบบประสาทรุนแรงขึ้น
โดยเฉลี่ยแล้วสมองของมนุษย์จะมีอุณหภูมิสูงกว่าอุณหภูมิของแกนกลางร่างกายไม่เกิน 1 องศาเซลเซียส แต่สมองของเราซึ่งเป็นหนึ่งในอวัยวะที่หิวโหยพลังงานในร่างกายจะสร้างความร้อนในตัวของมันเองอย่างมาก เมื่อเราคิด จดจำ และตอบสนองต่อโลกรอบตัวเรา นั่นหมายความว่าร่างกายของเราต้องทำงานหนักเพื่อให้สมองเย็นลง การไหลเวียนของเลือดผ่านเครือข่ายของหลอดเลือดจะช่วยรักษาอุณหภูมิของหลอดเลือดเพื่อขจัดความร้อนส่วนเกินให้ลดลงไป

กระบวนการนี้เป็นสิ่งจำเป็นเพราะเซลล์สมองไวต่อความร้อนอย่างมาก และคาดว่าฟังก์ชันของโมเลกุลบางตัวที่ส่งข้อความระหว่างกันก็ขึ้นอยู่กับอุณหภูมิเช่นกัน ซึ่งหมายความว่าฟังก์ชันเหล่านี้จะหยุดทำงานอย่างมีประสิทธิภาพหากสมองของเรามีอุณหภูมิร้อนเกินไปหรือเย็นเกินไป

"เรายังไม่เข้าใจอย่างถ่องแท้ว่าองค์ประกอบต่าง ๆ ของภาพที่ซับซ้อนของสมองได้รับผลกระทบอย่างไร" ซิโซดิยา กล่าว "แต่เราสามารถจินตนาการได้ว่ามันอาจเหมือนนาฬิกาที่ส่วนประกอบทั้งหมดไม่ได้ทำงานสอดประสานกันอีกต่อไป"

แม้ว่าความร้อนที่สูงจะเปลี่ยนวิธีการทำงานของสมองของทุกคน มันยังสามารถส่งผลเสียต่อความสามารถในการตัดสินใจและทำให้คนมีความเสี่ยงมากขึ้น ผู้ที่มีภาวะผิดปกติทางระบบประสาทมักจะได้รับผลกระทบรุนแรงที่สุดจากหลากหลายสาเหตุ ตัวอย่างเช่นในบางโรคที่การขับเหงื่อออกจากร่างกายอาจทำงานได้ไม่เต็มที่

"การไหลเวียนโลหิตเป็นการทำงานของสมองอย่างหนึ่งและอาจหยุดชะงักได้ หากบางส่วนของสมองทำงานอย่างไม่ถูกต้อง” ซิโซดิยา กล่าว

ตัวอย่างเช่นในผู้ป่วยโรคปลอกประสาทเสื่อมแข็ง (multiple sclerosis) บางชนิด อุณหภูมิแกนกลางของร่างกายดูเหมือนจะเกิดความผิดปกติ นอกจากนี้ ยาบางชนิดที่รักษาโรคทางระบบประสาทและโรคทางจิตเวช เช่น โรคจิตเภท (schizophrenia) ต่างก็มีผลต่อการควบคุมอุณหภูมิ ทำให้ผู้ที่รับความร้อนเข้าไปมีความเสี่ยงต่อโรคลมแดด หรือเกิดภาวะความร้อนสูงตามที่ทราบกันในทางการแพทย์ และมีความเสี่ยงที่จะเสียชีวิตจากความร้อนมากขึ้น

