คำบรรยายภาพ,เมื่ออุณหภูมิโลกสูงขึ้นพร้อมกับการเปลี่ยนแปลงสภาพภูมิอากาศจะทำให้ร่างกายและสมองของเราต้องเผชิญกับความเครียดมากขึ้น
คลื่นความร้อนสามารถส่งผลกระทบต่อปัจจัยต่าง ๆ ได้มากมาย ตั้งแต่การกระตุ้นไฟฟ้าของเซลล์ประสาทไปจนถึงความเสี่ยงต่อการฆ่าตัวตาย ความวิตกกังวลจากสภาพอากาศ และแม้แต่ความคงตัวของยาที่ใช้รักษาอาการทางระบบประสาท

แต่นักวิทยาศาสตร์ยังคงศึกษาผลกระทบของอุณหภูมิที่สูงขึ้นต่อสมองของเราอยู่ ความร้อนส่งผลกระทบต่อผู้คนในรูปแบบที่แตกต่างกันมาก บางคนสามารถดำรงชีวิตได้ดีในสภาพอากาศร้อน ในขณะที่บางคนรู้สึกว่าทนกับความร้อนในระดับนี้ไม่ได้

"ปัจจัยที่แตกต่างกันอาจเกี่ยวข้องกับความไวที่แตกต่างกัน และหนึ่งในนั้นอาจเป็นความอ่อนไหวทางพันธุกรรม" ซันเจย์ ซิโซดิยา ผู้เชี่ยวชาญจากยูซีแอลระบุ

ความแปรปรวนทางพันธุกรรมอาจมีอิทธิพลต่อโครงสร้างของโปรตีน ซึ่งอาจทำให้บางคนมีความเสี่ยงต่อผลกระทบของการเปลี่ยนแปลงสภาพภูมิอากาศมากขึ้น

"อาจมีลักษณะทางกายภาพจากพันธุกรรมที่แฝงความร้อนบางชนิด ซึ่งมันจะปรากฏออกมาได้ก็ต่อเมื่อแรงกดดันจากสิ่งแวดล้อมมากเพียงพอที่จะทำให้ลักษณะเหล่านั้นแสดงออกมา" เขากล่าว พร้อมเสริมว่า "สิ่งที่เราเห็นในปัจจุบันในผู้ที่มีความผิดปกติทางระบบประสาทอาจมีความสำคัญสำหรับผู้ที่ไม่มีความผิดปกติทางระบบประสาทเมื่อการเปลี่ยนแปลงสภาพภูมิอากาศดำเนินไป"

ยังมีคำถามอื่น ๆ ที่ยังรอคำตอบอยู่ ตัวอย่างเช่น อุณหภูมิสูงสุด ความยาวของคลื่นความร้อน หรืออุณหภูมิกลางคืน ส่งผลกระทบมากที่สุดหรือไม่ ซึ่งอาจแตกต่างกันไปตามแต่ละบุคคลหรือตามภาวะทางระบบประสาทที่แต่ละคนมี

แต่การระบุผู้ที่มีความเสี่ยงและสาเหตุจะเป็นสิ่งสำคัญในการพัฒนากลยุทธ์เพื่อปกป้องกลุ่มที่เปราะบางที่สุด ซึ่งอาจรวมถึงระบบเตือนภัยล่วงหน้าหรือประกันภัยเพื่อชดเชยค่าจ้างที่สูญเสียไปเนื่องจากความร้อนจัด

"ยุคโลกร้อนสิ้นสุดลงแล้ว ยุคแห่งความร้อนระอุของโลกได้มาถึงแล้ว" อันโตนิโอ กูเตอร์เรส เลขาธิการสหประชาชาติ ประกาศ เมื่อเดือน ก.ค. 2023 ซึ่งได้รับการยืนยันว่าเป็นเดือนที่ร้อนที่สุดเป็นประวัติการณ์

การเปลี่ยนแปลงสภาพภูมิอากาศมาถึงแล้วและกำลังทวีความรุนแรงขึ้น ทว่ายุคแห่งสมองร้อนเพิ่งเริ่มต้นขึ้น
