เมื่อมองขึ้นไปบนท้องฟ้ายามราตรีที่เต็มไปด้วยหมู่ดาวส่องแสงเจิดจ้า หลายคนอาจเคยถามตัวเองว่า จริงหรือที่มนุษย์โลกคือสิ่งมีชีวิตทรงภูมิปัญญาหนึ่งเดียวในห้วงจักรวาลอันไพศาล ซึ่งอาจจะกว้างใหญ่เกินกว่าที่เราจะสำรวจทำความเข้าใจได้ทั้งหมด ?

คำตอบของคำถามข้างต้นนั้น อาจเป็นไปได้ว่าเราไม่ได้อยู่อย่างโดดเดี่ยวตามลำพังในห้วงจักรวาล เพราะโลกนั้นเป็นเพียงจุดเล็ก ๆ จุดหนึ่ง ในมหาสมุทรของดวงดาวที่มีอยู่หลายพันล้านดวง ดังนั้นจะเป็นไปได้อย่างไรว่า โลกคือดาวเคราะห์ที่มีสิ่งมีชีวิตเพียงแห่งเดียวเท่านั้น ?

เหล่าผู้เชี่ยวชาญในเรื่องของสิ่งมีชีวิตนอกโลกต่างบอกว่า แม้จะยังไม่มีหลักฐานยืนยันอย่างชัดเจนว่า "เอเลียน" มีตัวตนอยู่จริง แต่ด้วยหลักการของความน่าจะเป็นทางคณิตศาสตร์ข้างต้น เราจำต้องยอมรับข้อสรุปที่ว่า สิ่งมีชีวิตนอกโลกจะต้องมีอยู่บนดาวดวงใดดวงหนึ่งอย่างแน่นอน เพราะแค่ในกาแล็กซีทางช้างเผือกเพียงแห่งเดียว ก็มีดวงดาวจำนวนมหาศาลถึง 300,000 ล้านดวงแล้ว ยิ่งไปกว่านั้น กาแล็กซีของเราก็เป็นแค่หนึ่งใน 200,000 ล้านดาราจักร ที่นักวิทยาศาสตร์ประมาณการว่ามีอยู่ในเอกภพ

ทุกวันนี้นักดาราศาสตร์ยังค้นพบ "ดาวเคราะห์นอกระบบสุริยะ" (exoplanet) ที่มีสภาพแวดล้อมเอื้อต่อการดำรงอยู่ของสิ่งมีชีวิตเพิ่มขึ้นเรื่อย ๆ ซึ่งดร.แม็กกี อาเดอริน-โพค็อก นักวิทยาศาสตร์อวกาศคนดังของสหราชอาณาจักร ได้แสดงความเห็นต่อประเด็นนี้ว่า "พวกเราค่อนข้างเชื่อมั่นว่าสิ่งมีชีวิตต่างดาวจะต้องอยู่ที่นั่นแน่ มันเป็นเรื่องของเกมจำนวนล้วน ๆ และมันคือความน่าจะเป็น (probability)"

