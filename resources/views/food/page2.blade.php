คำบรรยายภาพ,หากอารยธรรมของเราไม่ทับซ้อนกัน เราก็จะไม่มีวันได้พบเจอผู้คนจากดาวดวงอื่น ดร.แม็กกี อาเดอริน-โพค็อก ระบุ
ในอดีตนักวิทยาศาสตร์เชื่อกันว่า ชีวิตจะดำรงอยู่ได้เฉพาะบนดาวเคราะห์ ซึ่งโคจรอยู่ห่างจากดาวฤกษ์ในระยะที่เหมาะสมเท่านั้น เพราะมีเพียงดาวเคราะห์ดังกล่าวที่จะได้รับรังสีหรือพลังงานจากดาวฤกษ์ ในระดับที่ไม่มากหรือน้อยเกินไปจนเป็นอันตรายต่อสิ่งมีชีวิต ทว่าการค้นพบสิ่งมีชีวิตบนโลกในสถานที่ที่มันไม่ควรอยู่ ทำให้เราเล็งเห็นถึงความเป็นไปได้ว่า อาจมีสิ่งมีชีวิตบนดาวบางประเภทที่เราเคยมองข้ามไป อย่างเช่นดวงจันทร์บริวารของดาวเคราะห์ต่าง ๆ

อย่างไรก็ตาม ใช่ว่าสิ่งมีชีวิตต่างดาวที่กำลังจะถูกค้นพบนี้ จะต้องเป็นสิ่งมีชีวิตทรงภูมิปัญญารูปร่างคล้ายมนุษย์ อย่างที่เรามักจินตนาการกันถึงเอเลียนในภาพยนตร์ไซ-ไฟ บรรดาผู้เชี่ยวชาญเตือนว่า โอกาสที่จะพบสิ่งมีชีวิตต่างดาวนั้นมีอยู่สูงจริง ทว่าเป็นการยากยิ่งหรือแทบจะเป็นไปไม่ได้เลย ที่มนุษย์จะได้พบสิ่งมีชีวิตทรงภูมิปัญญาในระดับทัดเทียมกันจากดาวดวงอื่น

"เมื่อพิจารณาถึงประวัติความเป็นมาของสิ่งมีชีวิตบนโลก ช่วงเวลาส่วนใหญ่ในอดีตนับแต่ยุคบรรพกาลเป็นต้นมา โลกมีแต่สิ่งมีชีวิตแบบเรียบง่าย อันที่จริงแล้ว ชีวิตบนโลกเริ่มต้นด้วยการเป็นแบคทีเรียอยู่นานถึงหลายพันล้านปีเลยทีเดียว" ศ.โอไบรอันกล่าว

ยิ่งไปกว่านั้น สิ่งมีชีวิตโครงสร้างแบบเรียบง่ายไม่ซับซ้อน ยังต้องอาศัย "เหตุบังเอิญ" หลายครั้ง ในเส้นทางวิวัฒนาการ กว่าจะกลายมาเป็นสิ่งมีชีวิตหลายเซลล์บนโลกได้ ดังนั้นหากเราหวังจะพบเอเลียนที่ทรงภูมิปัญญาและมีอารยธรรมระดับสูง ก็คงต้องรอคอยให้สิ่งมีชีวิตเซลล์เดียวที่ดาวดวงอื่น มีวิวัฒนาการทางร่างกายและสติปัญญา จนสามารถสร้างเทคโนโลยีที่ล้ำสมัยได้เสียก่อน

จะมีแขกจากต่างดาวมาเยือนโลกไหม ?
ในเมื่อมีความเป็นไปได้สูงว่าเราไม่ได้อยู่โดดเดี่ยวในห้วงจักรวาล ดังนั้นเราควรจะตั้งตาคอยการมาเยือนของเอเลียน ซึ่งอาจจะเกิดขึ้นในเร็ววันนี้หรือไม่ ? เรื่องนี้ซับซ้อนและให้คำตอบที่ชัดเจนได้ยาก

แม้จะเป็นเรื่องเหลือเชื่อว่า ไม่เคยมีสิ่งมีชีวิตเผ่าพันธุ์ใดเลยในจักรวาลนี้ ที่สามารถพัฒนาตนเองจนเดินทางข้ามห้วงอวกาศระหว่างดวงดาวได้ แต่ในความเป็นจริง เราก็ยังไม่เคยพบเจอเอเลียนที่นั่งยานอวกาศมาเยือนโลกเลยสักครั้ง

"ปัญหาใหญ่ที่สุดในการค้นหาสิ่งมีชีวิตต่างดาวก็คือ เรามีตัวอย่างของสิ่งมีชีวิตที่เคยพบเจอมาเพียงแบบเดียว นั่นก็คือสิ่งมีชีวิตบนโลกใบนี้" ดร.อาเดอริน-โพค็อก กล่าว

นั่นหมายความว่ามนุษย์ไม่สามารถจินตนาการถึงสิ่งมีชีวิตรูปแบบอื่น ที่เราไม่เคยพบเห็นมาก่อนได้ "ตัวอย่างเช่น หากคุณอาศัยอยู่บนดาวที่มีความเคลื่อนไหวเปลี่ยนแปลงรุนแรง คุณอาจจะต้องลงไปอยู่ใต้ดิน และไม่อาจจะส่งสัญญาณใด ๆ ออกมาจากใต้พิภพได้ ทำให้ดูเหมือนกับว่าดาวดวงนั้นไม่มีสิ่งมีชีวิตอยู่เลย แต่อันที่จริงอาจมีสิ่งมีชีวิตทรงภูมิปัญญาหลบซ่อนตัวอยู่ก็เป็นได้" ดร.อาเดอริน-โพค็อก อธิบาย

