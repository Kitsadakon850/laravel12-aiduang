มีการใช้กล้องโทรทรรศน์ที่ทรงพลัง ส่องดูแสงจากดาวฤกษ์ที่ฉายผ่านชั้นบรรยากาศของดาวเคราะห์นอกระบบสุริยะ ในตอนที่มันเคลื่อนผ่านตัดหน้าดาวฤกษ์ศูนย์กลาง การตรวจวิเคราะห์สเปกตรัมของแสงที่ปรากฏ (spectroscopy) จะช่วยให้ทราบได้ว่า ดาวเคราะห์ดังกล่าวมีแร่ธาตุอะไรเป็นองค์ประกอบอยู่บ้าง ซึ่งหากพบดาวดวงที่มีองค์ประกอบคล้ายกับโลก ก็อาจจะสันนิษฐานได้ว่าดาวเคราะห์นอกระบบสุริยะดังกล่าว มีสภาพแวดล้อมที่เอื้อต่อการดำรงอยู่ของสิ่งมีชีวิตด้วยเช่นกัน แม้จะอยู่ห่างไกลออกไปจากโลกถึงหลายพันปีแสงก็ตาม

ศาสตราจารย์ทิม โอไบรอัน นักฟิสิกส์ดาราศาสตร์ จากมหาวิทยาลัยแมนเชสเตอร์ของสหราชอาณาจักร บอกกับบีบีซีว่า "ปัจจุบันเรารู้จักดาวเคราะห์นอกระบบสุริยะ ที่สิ่งมีชีวิตน่าจะอยู่อาศัยได้หลายร้อยดวงแล้ว เรามั่นใจเกือบร้อยเปอร์เซ็นต์ว่าภายในช่วงทศวรรษหน้า จะได้พบกับดาวเคราะห์ที่มีร่องรอยหลักฐานของสิ่งมีชีวิตปรากฏอยู่อย่างชัดเจน"

หลักฐานที่บ่งชี้ถึงความเป็นไปได้ดังกล่าว สามารถพบได้แม้แต่บนโลกของเราเอง โดยก่อนหน้านี้มีการค้นพบสิ่งมีชีวิตในสภาพแวดล้อมอันโหดร้ายมาแล้ว เช่นในสถานที่มืดมิดปราศจากแสงอาทิตย์และความอบอุ่น อย่างที่ก้นร่องลึกของมหาสมุทร

