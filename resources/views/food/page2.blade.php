คำบรรยายภาพ,ข้อความใด ๆ ที่เราพยายามส่งไปถึงสิ่งมีชีวิตอื่นอาจต้องใช้เวลานับพันหรือหลายล้านปีจึงจะไปถึง นักวิทยาศาสตร์กล่าว
นอกจากนี้ยังมีความเป็นไปได้ในทางวิทยาศาสตร์ว่า มนุษย์เรากับเอเลียนต่างไม่เข้าใจภาษาของฝ่ายตรงข้าม ทั้งไม่อาจถอดรหัสหรือรับสัญญาณที่อีกฝ่ายส่งออกมาได้อีกด้วย "เราใช้กล้องโทรทรรศน์วิทยุ ดักฟังสัญญาณจากอารยธรรมต่างดาวมานาน ตั้งแต่ช่วงทศวรรษ 1960 แล้ว" ศ.โอไบรอันกล่าว ทว่าเอเลียนก็อาจจะส่งสัญญาณในรูปแบบอื่นที่ไม่ใช่คลื่นวิทยุออกมาได้เช่นกัน

หรือถึงแม้มนุษย์กับเอเลียนจะสื่อสารด้วยสัญญาณในช่วงคลื่นที่ตรงกัน แต่สัญญาณดังกล่าวจะต้องใช้เวลาเดินทางข้ามห้วงอวกาศอันกว้างใหญ่ไพศาลอย่างยาวนาน ซึ่งอาจเป็นเวลาถึงหลายพันหลายหมื่นปี กว่าที่อีกฝ่ายจะได้รับสารและตอบกลับมา ซึ่งคิดไปแล้วก็ไม่ต่างกับการสื่อสารด้วยจดหมายที่เชื่องช้ามากในสมัยโบราณ

ที่ผ่านมาโครงการ Breakthrough Listen ซึ่งตั้งอยู่มหาวิทยาลัยแคลิฟอร์เนีย วิทยาเขตเบิร์กลีย์ของสหรัฐฯ (UC Berkeley) ได้ดักฟังคลื่นวิทยุจากดาวเคราะห์ประมาณหนึ่งล้านดวงใกล้โลก เพื่อค้นหาว่ามีดาวดวงใดบ้าง ที่ปรากฏร่องรอยการส่งสัญญาณตอบกลับมายังโลก นอกจากนี้ยังสำรวจระบบดาวฤกษ์บริเวณใจกลางกาแล็กซีทางช้างเผือก ซึ่งอยู่ห่างจากโลกราว 25,000 ปีแสงด้วย

นั่นหมายความว่า หากมีสิ่งมีชีวิตทรงภูมิปัญญาต่างดาวอยู่ที่นั่นจริง สัญญาณที่ส่งมาจากพวกเขาจะต้องใช้เวลาเดินทางราว 25,000 ปี กว่าจะมาถึงโลก ดังนั้นเราอาจจะต้องรอคอยนานถึงหลายพันหลายหมื่นปี กว่าจะมีโอกาสได้ติดต่อสื่อสารกับเอเลียนสักครั้ง

ยังอีกนานกว่าจะได้เจอกัน
หากการสื่อสารทางไกลผ่านคลื่นวิทยุกับเอเลียนนั้นทำได้ยากยิ่ง ก็ไม่ต้องพูดถึงการเดินทางข้ามห้วงอวกาศ เพื่อมาพบเจอกันแบบตัวเป็น ๆ ในอนาคตอันใกล้ เพราะยังไม่มียานพาหนะชนิดใด ที่สามารถเดินทางข้ามห้วงอวกาศได้อย่างรวดเร็วพอ

ส่วนการ "วอร์ป" (warp) หรือการย่นระยะทางผ่านมิติของปริภูมิ-เวลา รวมทั้งการส่งสิ่งของและผู้คนไปยังอีกมุมหนึ่งของห้วงจักรวาลในพริบตานั้น ยังคงเป็นเพียงจินตนาการที่ท้าทายการพัฒนาทางเทคโนโลยี ซึ่งปัจจุบันก็ยังไล่ตามไม่ทันความฝันของคนเรา

มีความเป็นไปได้ว่า หากอารยธรรมที่มีมานานหลายพันปีของมนุษย์ ยังไม่สามารถสร้างยานอวกาศหรือวิธีการเดินทางที่ข้ามห้วงอวกาศได้อย่างมีประสิทธิภาพ อารยธรรมของสิ่งมีชีวิตทรงภูมิปัญญาที่ดาวดวงอื่น ๆ ก็อาจจะยังทำไม่ได้ด้วยเหมือนกัน หรือถึงแม้พวกเขาจะทำได้สำเร็จไปก่อนหน้านี้นานแล้ว ก็ยังเป็นไปได้ว่าเอเลียนนั้น ไม่ต้องการจะเดินทางมาพบกับเราเลย ไม่ว่าจะด้วยเหตุผลอะไรก็ตามแต่

นอกจากนี้ การพบปะและติดต่อสื่อสารระหว่างมนุษย์กับเอเลียน ยังต้องอาศัยโชคช่วยอย่างมาก เพราะช่วงเวลาการดำรงอยู่ของเผ่าพันธุ์และอารยธรรมมนุษย์ อาจไม่ตรงกับช่วงเวลาที่เอเลียนถือกำเนิดและพัฒนาเทคโนโลยีขั้นสูงก็เป็นได้

อย่าลืมว่าสิ่งมีชีวิตเกิดขึ้นและอยู่บนโลกใบนี้มานานถึง 3,500 ล้านปี แต่มนุษย์ยุคใหม่ที่มีสติปัญญาระดับสูง เพิ่งปรากฏตัวขึ้นเมื่อราว 300,000 ปีก่อนเท่านั้น ทั้งยังมีความเสี่ยงที่อารยธรรมของมนุษย์จะดำรงอยู่แค่ในช่วงสั้น ๆ ก่อนจะดับสูญไปในที่สุด

เนื่องจากการเกิดขึ้นและสูญสลายของอารยธรรม สามารถดำเนินไปในชั่วพริบตา เมื่อเทียบกับการดำรงอยู่อย่างยาวนานกว่าหมื่นล้านปีของห้วงจักรวาล เราจึงไม่อาจจะล่วงรู้หรือแน่ใจได้ว่า เอเลียนเคยมาเยือนโลกของเราในอดีตที่ผ่านมาหรือไม่ "หากอารยธรรมของเรากับเอเลียน ไม่ได้ดำรงอยู่ในช่วงเวลาที่ทับซ้อนกัน ทั้งสองจะไม่มีโอกาสได้พบกันเลย" ดร.อาเดอริน-โพค็อก กล่าว

ด้วยเหตุนี้ เอเลียนอาจมาเยือนโลกในอนาคต ในตอนที่เผ่าพันธุ์มนุษย์ทั้งหมดอำลาจากไปแล้ว หรืออาจจะเคยมาเยือนโลกยุคดึกดำบรรพ์ ในตอนที่มนุษย์ยังไม่ถือกำเนิดขึ้นก็เป็นได้ ซึ่งหากเป็นเช่นนั้นจริง ไดโนเสาร์ในยุคจูราสสิก อาจจะเคยทำหน้าที่เจ้าบ้านผู้ต้อนรับแขกจากต่างดาวมาแล้ว เมื่อหลายสิบล้านปีก่อน</h3>
